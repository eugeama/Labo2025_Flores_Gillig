package SistemaPoblacion;

public class Coordenadas {
    private int alto;
    private int largo;

    public Coordenadas(int alto, int largo){
        this.alto= alto;
        this.largo= largo;
    }

    public int getAlto() {
        return alto;
    }

    public void setAlto(int alto) {
        this.alto = alto;
    }

    public int getLargo() {
        return largo;
    }

    public void setLargo(int largo) {
        this.largo = largo;
    }
}
